<h2>Как же выбрать психолога-консультанта, работающего он-лайн</h2>
В интернете кого только нет. <br>
Какие можно выделить критерии по выбору хорошего консультанта:<br><br>

		<div class="num">1</div> <b>Наличие диплома специалиста-психолога или сертификатов по смежным профессиям (гипноз, терапия, НЛП)</b><br>

Это не гарантирует, конечно, качество консультации, но защищает от совсем случайных людей
<br><br>
		<div class="num">2</div>	<b>Хороший психолог не согласится работать по Skype без включенной видеокамеры</b><br>

Терапевт должен видеть, что происходит с клиентом. Это обязательное условие экологичной консультации по Скайп.
<br><br>
		<div class="num">3</div>	<b>Наличие сайта, наполненных соц.сетей</b><br>

Чем дольше специалист занимается своим продвижением, тем больше накапливается контактов в соц.сетях, роликов на youtube, отзывов о работе, сертификатов об окончании тренингов и курсов повышения квалификации, ответов на часто задаваемые вопросы и так далее. Одним словом, наращивается присутствие специалиста в сети.
Всё это можно и нужно изучать при выборе специалиста.
Чем больше присутствие психолога в сети, тем предположительно больше его опыт. Чем дольше специалист занимается предметом, тем больше вероятность попасть к хорошему специалисту, все мы так или иначе растём профессионально при увеличении потраченного на предмет времени. 

