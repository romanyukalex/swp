<b>Чтобы быть проницательным — большого ума не надо</b>. Надо лишь сосредоточиться на том, что действительно несет информацию , а не на том, что отвлекает. А для того, чтобы ваш собеседник не наблюдал, а отвлекался, для этого вы открываете рот и начинаете говорить. И в это время собираете нужную информацию. <br>
