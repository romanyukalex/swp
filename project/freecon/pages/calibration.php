		<br><b>Ступни</b><br><br>

Сколько из вас замечают ступни? Многие люди считают, что в твердом уме и трезвой памяти на ноги смотреть никто не будет. Еще, понимаешь, под стол лезть… Так что движение ног человек обычно не контролирует и не думает, что за ними кто-то следит.<br>
Между тем, у ног свой рисунок движения, своя ритмика . А правая и левая еще, бывает, по-разному двигаются. В каждый момент времени мы не знаем, что именно влияет на движения ног, но, когда это происходит, мы можем это заметить. А когда мы получаем информацию, можем начать воздействие. <br>

<br><b>Голеностоп</b><br><br>

Там мало что заметно и мало что происходит. Единственное, что там очень хорошо заметно даже по одежде, — напряжены икры или расслаблены . И если напряжены, то в какой части. Там тоже могут проскальзывать мелкие движения. <br>

<br><b>Колено</b><br><br>

Самый заметный маркер на ноге. Там самая большая амплитуда движения.
Когда человек не сидит, а стоит, именно по состоянию коленей мы можем наблюдать, какая из ног расслаблена . Но человек их регулярно меняет, а момент смены  нас регулярно и интересует. <br>
Если у мужчин брюки со стрелкой, эта стрелка  позволяет заметить мелкие движения ног. Но если человек в шортах или мини-юбке, это уже счастье — чего еще надо. Тогда нам становятся заметны и изменения в цвете . На ногах заметны изменения цвета: куда переливается кровь, где находятся очаги расслабления и напряжения. <br>
Или «гусиная кожа ». <br>

<br><b>Бедро</b><br><br>

Когда человек сидит или стоит, бедра во многом связаны с его плечами . Макродвижения бедрами повторяются плечами. Когда человек и бедром, и плечом делает движение — это естественно. <br>
В нашей культуре в район бедер смотреть не принято. По крайней мере, в упор. Но если мы периферически туда посмотрим, мы очень много можем там увидеть. Плечи человек контролирует — это социальная часть тела. Бедра ходят даже у сидячего, а уж как у стоячего они ходят— посмотрите. <br>
У ходячего можно уловить изменение внутреннего состояния по смене походки. Там много сразу заметных маркеров. <br>

<br><b>Дыхание</b><br><br>

Обычно дыхание видно, как ритмическую смену света и тени на одежде : на лацканах, на складочках. Хуже всего дыхание видно у стройных и худых, у мужчин в темном. Чем человек полнее, чем больше у него грудь и чем светлее то, что на нем надето, тем лучше видно дыхание. <br>
Кто-то дышит глубоко, и приходится ждать минуту, пока изменится рисунок света и тени. Кто-то чаще. <br>

<br><b>Руки</b><br><br>

Как брови и глаза, во всех крупных движениях руки демонстрируют сознательные паттерны: то, что человек показывает, а не то, что есть на самом деле. <br>
Но руки могут подергиваться . Этакое начатое и не законченное движение. Такие движения начинают нас интересовать. <br>
Иногда люди могут хрустеть пальцами . И это может нас интересовать. <br>
Это, пожалуй, и все. <br>
То, что можно заметить по рукам, чаще всего можно заметить и в другом месте. Но иногда бывает, что только в руках и больше нигде у человека есть реакция. Все тело неподвижно и только руки шевелятся — кисти или пальцы. <br>
Или человек вертит что-то в руках: быстрее, медленнее, смена ритма, остановка, продолжение. <br>


<br><b>Плечи</b><br><br>

Помимо ритма дыхания, плечи — большой индикатор расслабленности  или напряженности человека в целом. Любое движение рук начинается в плечах . Иногда человек успевает удержать руки от движений — но плечи-то уже дернулись…<br>

<br><b>Голова и шея</b><br><br>

При определенной наблюдательности вы можете заметить напряжение  в шее.
Шея двигается. И это важно, если у вас нет возможности наблюдать человека спереди. Сзади шея достаточно живая, и по ней можно много чего заметить. И еще там может сильнее или слабее биться жилка. <br>
Голова — предмет социальный. Нас интересуют наклоны головы и повороты той или иной стороной лица к нам. Сам момент изменения, какой стороной лица человек на нас смотрит, нам очень интересен, потому что это свидетельство изменения его настроения по поводу нас и наших действий. <br>
Особо интересен поворот головы в комплексе с плечами . Плечи могут оставаться на месте, а голова меняться. Важно в комплексе смотреть на треугольник, образуемый макушкой и плечами. <br>
Одного волейболиста, мастера ставить блоки, спросили, как у него получается так замечательно ставить блоки. Он ответил: когда нападающий бьет, он может глазами показывать, ушами демонстрировать, что, мол, сейчас мы туда, а на самом деле сюда, но кончик его носа  всегда смотрит туда, куда будет нанесен удар. <br>
Так вот речь здесь идет скорее о повороте головы, поскольку нос от головы неотделим и поворачивается вместе с ней. <br>


